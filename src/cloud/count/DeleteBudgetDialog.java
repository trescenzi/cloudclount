    public void initDeleteBudgetTable()
    {
        final int[] WIDTHS = 
        {
            20,  // ID
            50,  // Status
            150,  //Title
        };
        
        TableColumnModel tableColumnModel = deleteBudgetsTable.getColumnModel();

        for (int i = 0; i < WIDTHS.length; i++) 
        {
            TableColumn col = deleteBudgetsTable.getColumnModel().getColumn(i);
            col.setPreferredWidth(WIDTHS[i]);
        }
        
        deleteBudgetsTable.addMouseListener(new MouseAdapter()
        {
           @Override
           public void mouseClicked(MouseEvent e)
           {
               if(e.getClickCount() == 1)
               {
                   JTable target = (JTable)e.getSource();
                   selectedRow = target.getSelectedRow();
               }
           }
        });
    }
    
