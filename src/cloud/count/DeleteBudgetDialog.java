    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed

        SwingUtilities.invokeLater(new Runnable()
        {
            public void run()
            {
                ArrayList<Budget> budgets = Budget.all();
                Budget budget = budgets.get(selectedRow);
                int budgetID = budget.getId();

                // Confirm the delete
                if (JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this budget?", "Choose One", JOptionPane.YES_NO_OPTION) == 0)
                {
                    Budget.find(budgetID).delete();

                    // Auto refresh
                    DeleteBudgetTableModel deleteBudgetModel = (DeleteBudgetTableModel)deleteBudgetsTable.getModel();
                    deleteBudgetModel.refresh();
                }
            }
        });
    }//GEN-LAST:event_deleteButtonActionPerformed

